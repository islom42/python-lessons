# Foydalanuvchi kiritgan sonning kvadrati va kubini konsolga chiqaruvchi dastur
son = int(input("Please enter a number\n>>>"));
print(f"Siz kiritgan son kvadrati {son **2}ga teng")
print(f"Siz kiritgan son kvadrati {(son **2)*son}ga teng")

